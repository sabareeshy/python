num1 = 10
num2 = 5

def addition():
    add = num1 + num2
    print("addition of the numbers : " + str(add))

def subtraction():
    sub = num1 - num2
    print("subtraction of the numbers : " + str(sub))

def multiplication():
    mul = num1 * num2
    print("multiplication of the numbers : " + str(mul))

addition()
subtraction()
multiplication()